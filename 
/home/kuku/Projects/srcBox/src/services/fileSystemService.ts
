
import { Note, NoteInput, Folder } from '../types/note';
import { createSlug } from '../utils/slug';
import yaml from 'js-yaml';

interface FileSystemState {
  rootPath: string | null;
  isInitialized: boolean;
}

const isElectron = typeof window !== 'undefined' && (window as any).electronAPI?.selectFolder;
declare global {
  interface Window {
    electronAPI: any;
  }
}

class FileSystemStorage {
  private state: FileSystemState = {
    rootPath: null,
    isInitialized: false,
  };

  async initialize(): Promise<boolean> {
    try {
      if (isElectron) {
        const result = await (window as any).electronAPI.selectFolder();
        if (result.success && !result.canceled) {
          this.state.rootPath = result.path;
          this.state.isInitialized = true;
          localStorage.setItem('srcbox-folder-path', result.path);
          return true;
        }
        return false;
      } else {
        const handle = await (window as any).showDirectoryPicker({ mode: 'readwrite' });
        this.state.rootPath = handle.name;
        this.state.isInitialized = true;
        localStorage.setItem('srcbox-directory-handle', 'true');
        return true;
      }
    } catch (error) {
      console.error('Failed to initialize file system:', error);
      return false;
    }
  }

  async tryRestoreConnection(): Promise<boolean> {
    if (this.state.isInitialized && this.state.rootPath) return true;

    const storedPath = localStorage.getItem('srcbox-folder-path');
    if (storedPath && isElectron) {
      const result = await (window as any).electronAPI.checkFolderAccess(storedPath);
      if (result.accessible) {
        this.state.rootPath = storedPath;
        this.state.isInitialized = true;
        return true;
      }
      localStorage.removeItem('srcbox-folder-path');
    }
    return false;
  }

  isInitialized(): boolean {
    return this.state.isInitialized;
  }

  private get root(): string {
    if (!this.state.rootPath) {
      console.warn('FS root not available, using fallback');
      return '';
    }
    return this.state.rootPath;
  }

  private async saveNoteToFile(note: Note): Promise<void> {
    if (isElectron && this.root) {
      const frontmatter = yaml.dump({
        _id: note._id,
        slug: note.slug,
        title: note.title,
        tags: note.tags,
        deletedAt: note.deletedAt,
        createdAt: note.createdAt,
        updatedAt: note.updatedAt,
        type: note.type,
        folderPath: note.folderPath,
      });
      const content = `---\n${frontmatter}---\n\n${note.content || ''}`;
      await (window as any).electronAPI.writeNote(
        this.root,
        note._id,
        note.folderPath ?? null,
        content
      );
    } else {
      console.warn('Save skipped - not Electron or no root');
    }
  }

  async createNote(input: NoteInput): Promise<Note> {
    const now = new Date().toISOString();
    const title = input.title || 'Untitled';
    let slug = createSlug(title);
    
    const allNotes = await this.getAllNotes();
    let counter = 1;
    while (allNotes.some(n => n.slug === slug)) {
      slug = createSlug(title + '-' + counter);
      counter++;
    }
    
    const note: Note = {
      _id: slug,
      slug: slug,
      title,
      content: input.content || '',
      tags: input.tags || [],
      deletedAt: null,
      createdAt: now,
      updatedAt: now,
      type: 'note',
      folderPath: input.folderPath ?? null,
    };
    await this.saveNoteToFile(note);
    return note;
  }

  async getAllNotes(): Promise<Note[]> {
    if (!this.state.isInitialized || !isElectron || !window.electronAPI) {
      console.log('getAllNotes skipped - not ready');
      return [];
    }
    try {
      const result = await (window as any).electronAPI.readNotes(this.root);
      console.log('readNotes result:', result);
      if (result.success) {
        return (result.notes as Note[]).sort(
          (a, b) => new Date(b.updatedAt).getTime() - new Date(a.updatedAt).getTime()
        );
      }
      return [];
    } catch (error) {
      console.error('getAllNotes error:', error);
      return [];
    }
  }

  async getNote(id: string): Promise<Note | null> {
    const all = await this.getAllNotes();
    return all.find((n) => n._id === id) ?? null;
  }

  async updateNote(id: string, updates: Partial<NoteInput>): Promise<Note> {
    if (!this.state.isInitialized) {
      throw new Error('File system not ready for update');
    }
    const existing = await this.getNote(id);
    if (!existing) {
      console.warn(`Note ${id} not found for update, creating new (FS:${this.state.rootPath})`);
    }
    const updated: Note = existing ? 
      { ...existing, ...(updates as Partial<Note>), updatedAt: new Date().toISOString() } :
      {
        _id: id,
        slug: id,
        title: updates.title || 'Untitled',
        content: updates.content || '',
        tags: [],
        deletedAt: null,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        type: 'note' as const,
        folderPath: null,
        ...updates as Partial<Note>
      };
    await this.saveNoteToFile(updated);
    return updated;
  }

  // ... rest unchanged
  async deleteNote(id: string): Promise<void> {
    const note = await this.getNote(id);
    if (isElectron) {
      await (window as any).electronAPI.deleteNote(this.root, id, note?.folderPath ?? null);
    }
  }

  async softDeleteNote(id: string): Promise<Note> {
    return this.updateNote(id, { deletedAt: new Date().toISOString() } as any);
  }

  async restoreNote(id: string): Promise<Note> {
    return this.updateNote(id, { deletedAt: null } as any);
  }

  async moveNote(id: string, targetFolderPath: string | null): Promise<Note> {
    const note = await this.getNote(id);
    if (!note) throw new Error('Note not found');
    if (isElectron) {
      await (window as any).electronAPI.moveNote(this.root, id, note.folderPath ?? null, targetFolderPath);
    }
    const updated: Note = { ...note, folderPath: targetFolderPath, updatedAt: new Date().toISOString() };
    await this.saveNoteToFile(updated);
    return updated;
  }

  async getActiveNotes(): Promise<Note[]> {
    const all = await this.getAllNotes();
    return all.filter((n) => !n.deletedAt);
  }

  async getTrashedNotes(): Promise<Note[]> {
    const all = await this.getAllNotes();
    return all.filter((n) => n.deletedAt !== null);
  }

  async searchNotes(query: string): Promise<Note[]> {
    const active = await this.getActiveNotes();
    const q = query.toLowerCase();
    return active.filter(
      (n) =>
        n.title.toLowerCase().includes(q) ||
        n.content.toLowerCase().includes(q) ||
        n.tags.some((t) => t.toLowerCase().includes(q))
    );
  }

  async getNotesByTag(tag: string): Promise<Note[]> {
    const active = await this.getActiveNotes();
    return active.filter((n) => n.tags.includes(tag));
  }

  async getAllTags(): Promise<string[]> {
    const active = await this.getActiveNotes();
    const set = new Set<string>();
    active.forEach((n) => n.tags.forEach((t) => set.add(t)));
    return Array.from(set).sort();
  }

  async getFolders(): Promise<Folder[]> {
    if (!this.state.isInitialized || !isElectron || !window.electronAPI) {
      return [];
    }
    try {
      const result = await (window as any).electronAPI.readFolders(this.root);
      if (result.success) return result.folders as Folder[];
      return [];
    } catch {
      return [];
    }
  }

  async createFolder(parentPath: string | null, name: string): Promise<Folder> {
    if (isElectron) {
      const result = await (window as any).electronAPI.createFolder(this.root, parentPath, name);
      if (result.success) return result.folder as Folder;
      throw new Error(result.error || 'Failed to create folder');
    }
    throw new Error('Web file system not implemented');
  }

  async renameFolder(folderPath: string, newName: string): Promise<string> {
    if (isElectron) {
      const result = await (window as any).electronAPI.renameFolder(this.root, folderPath, newName);
      if (result.success) return result.newPath as string;
      throw new Error(result.error || 'Failed to rename folder');
    }
    throw new Error('Web file system not implemented');
  }

  async deleteFolder(folderPath: string): Promise<void> {
    if (isElectron) {
      const result = await (window as any).electronAPI.deleteFolder(this.root, folderPath);
      if (!result.success) throw new Error(result.error || 'Failed to delete folder');
    } else {
      throw new Error('Web file system not implemented');
    }
  }
}

export const fileSystemStorage = new FileSystemStorage();

